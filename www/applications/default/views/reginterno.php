<center><p><b>CAPITULO  I</b></p>
<p><b>DISPOSICIONES GENERALES</b></p></center>
<p>
<b>ARTÍCULO 1º.-</b> La Escuela de Enfermería de Apatzingán AC. A través de de la Dirección de la Escuela vigilará con sujeción a las normas de este Reglamento, el estricto cumplimiento de la Ley Orgánica de la Universidad Michoacana y de los reglamentos y disposiciones Universitarias que de ella emanen.
</p>
<p>
<b>ARTÍCULO 2º.-</b> La Escuela ofrecerá una carrera Universitaria de ENFERMERIA de seis semestres, y estará compuesto por materias autorizadas por la subdirección de Escuelas Incorporadas de la Universidad Michoacana.
</p>
<p>
<b>ARTÍCULO 3º.-</b> La Escuela de Enfermería tiene como objetivo, formar personal altamente profesionalizado en los servicios de salud para desempeñar el trabajo de Enfermería.
</p>
<p>
<b>ARTÍCULO 4º.-</b> Es deber y obligación del personal administrativo, profesores y alumnos, el cumplir y hacer cumplir el presente reglamento y todas y cada una de las leyes y reglamentos que derivada de la incorporación a la Universidad Michoacana resulten.
</p>
<p>&nbsp;</p>
<center>
<p><b>CAPÍTULO II</b></p>
<p><b>DEL GOBIERNO</b></p></center>
<p>
<b>ARTÍCULO 5º.-</b> El Gobierno de la Escuela de Enfermería estará integrado de la siguiente manera:
</p>
<p>
I.- La Asamblea del Consejo Directivo de Representantes de la Escuela de Enfermería de Apatzingán, Michoacán, A.C.
</p>
<p>
II.-  El Consejo Técnico de la Escuela de Enfermería
</p>
<p>
III.- El Director de la Escuela de Enfermería
</p>
<p>
IV.-  La Jefatura de Control Escolar
</p>
<p>
V.-   La Secretaría Administrativa, y
</p>
<p>
VI.-  La Secretaria Académica
</p>
<center><b>
<p>CAPÍTULO III</p>
<p>DEL CONSEJO DIRECTIVO DE REPRESENTANTES</p>
</b></center>

<p><b>ARTÍCULO 6º.-</b> Son facultades del Consejo Directivo de Representantes:</p>
<p>I.- El de Administración del Patrimonio de la Escuela de Enfermería.</p>
<p>II.- Nombrar al Director de la Escuela en base a la terna propuesta por el H. Consejo Técnico de la misma.</p>
<p>III.- Ratificar al director de la escuela, por así convenir a los intereses de la misma.</p>
<p>IV.- Las demás que se señale el acta constitutiva protocolizada.</p>
<p>&nbsp;</p>
<center><b>
<p>CAPITULO IV</p>
<p>DEL DIRECTOR</p>
</b></center>
<b>ARTICULO 7º.- </b>Para ser Director se requiere:
<p>I.- Ser mexicano por nacimiento</p>
<p>II.- Tener más de 30 años de edad</p>
<p>III.- Poseer titulo de  Licenciatura en Enfermería o Superior.</p>
<p>IV.- Haber desempeñado actividades docentes o de investigación por lo menos durante tres años y estar en ejercicio en el momento de su designación.</p>
<p>V.- Impartir por lo menos una asignatura en el plantel con la antigüedad que marca el artículo anterior.</p>
<p>VI.- Tener probada calidad moral.</p>

<p><b>ARTICULO 8º.-</b> Son facultades y deberes del Director:</p>
<p>I.- Cumplir y hacer cumplir la Ley Orgánica, el estatuto y reglamentos vigentes aplicables a la Escuela de Enfermería.</p>
<p>II.- Representar legalmente al plantel ante la Universidad</p>
<p>III.- Fungir como Presidente del H. Consejo Técnico de la escuela</p>
<p>IV.- No ostentar cargos administrativos en otra Institución Educativa</p>
<p>V.- Permanecer en el plantel en el desempeño de su cargo un mínimo de dos horas diarias por turno, sin considerar sus actividades docentes.</p>
<p>VI.- Impartir como mínimo una asignatura del plan de estudios correspondientes.</p>
<p>VII.- Proponer las designaciones, cambios y remociones a planta de profesores y supervisores, por lo menos treinta días antes del inicio de cursos, vigilando que cumplan los requisitos establecidos en el reglamento de incorporación.</p>
<p>VIII.- Supervisar que se cumplan los planes y programas de estudio y la asistencia a clases del personal docente.</p>
<p>IX.- Asistir a las reuniones ordinarias y extraordinarias que convoque la Subdirección de Escuelas Incorporadas, así como en las ocasiones en que sea requerido.</p>
<p>X.- Tramitar y firmar al inicio de cada Ciclo Escolar, las credenciales de Identificación del personal docente, administrativo y alumnado en general.</p>
<p>XI.- Formular ante su Consejo Técnico, el proyecto de reglamento interno y modificaciones para su aprobación, el que no deberá contravenir a los principios de la Ley Orgánica y Reglamentos de la Universidad Michoacana.</p>
<p>XII.- Dar a conocer a la Subdirección de Escuelas Incorporadas los nombramientos de personal con funciones de administración.</p>
<p>XIII.- Tramitar la asignación de las matriculas, a los alumnos que hayan satisfecho todos los requisitos reglamentarios.</p>
<p>XIV.- Tramitar en tiempo y forma toda la documentación requerida por la Universidad Michoacana.</p>
<p>XV.- Difundir el reglamento general interno.</p>
<p>XVI.- Delegar funciones de carácter administrativo en el personal que refiere el artículo XII.</p>
<p>XVII.- Entregar oportunamente los certificados expedidos por la Universidad.</p>
<p>XVIII.- Implementar mecanismos de control que coadyuven al desenvolvimiento académico en bien de la dependencia, y</p>
<p>XIX.- Las demás que le sean encomendadas por el Consejo Directivo de Representantes, el Consejo Técnico y la propia Universidad Michoacana.</p>
