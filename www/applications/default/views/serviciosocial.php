<p>Trámites y documentos para recibir plaza de servicio social que el alumno debe llevar personalmente a la ciudad de Morelia</P>
<ol>
  <li>3 fotografías tamaño infantil blanco y negro (con nombre completo al reverso a lápiz)</li>
</li><li>
 Acta de nacimiento reciente “color café” vigencia de 2012 (original y 2 copias)
</li><li>
  Memorandum de calificaciones vigencia del 2012 (original y 2 copias)
</li><li>
 Carta de pasante comercial (original y 2 copias)
</li><li>
 2 copias de la CURP tamaño carta
</li><li>
Certificado médico reciente original solo será valido de la cruz roja (30 días antes a la selección de plaza) no particulares, SSA, IMSS o ISSSTE
</li><li>
Comprobante de domicilio (agua, luz o teléfono fijo) no tira de pago o comprobante de pago vigencia de 2 meses (mayo/junio) (original y 2 copias)
</li><li>
Credencial de elector (2 copias ampliación a carta, terminación 03 no válida), menor de edad credencial de escuela (2 copias)
</li><li>
Pago al derecho de servicio social (concepto 1212) en las cajas de la tesorería de la universidad michoacana en la ciudad de Morelia.
</li><li>
Carta de antecedentes no penales expedida por la PGJE de Michoacán, no constancia municipal. Vigencia de junio 12
</li><li>
RFC con homoclave expedido por la SAT (original y copia)
</li><li>
Entrar a la página <a href="http://www.ensenanzamich.gob.mx">http://www.ensenanzamich.gob.mx</a>  copia del registro
</li><li>
Impresión de la hoja de registro federal <a href="http://dgces.salud.gob.mx/siass/">http://dgces.salud.gob.mx/siass/</a>
</li><li>
La documentación deberá ser entregada en un folder color paja, tamaño carta, rotulado con máquina de escribir en la pestaña de la siguiente manera: nombre (comenzando con apellido paterno, apellido materno y nombre(s), carrera y promoción. Ejemplo "Aguilar cervantes María Marilin, enfermería, agosto 2012”
</li><li>
Formulario servicio social para los reportes bimensuales a la escuela de enfermería.
</li><li>
Presentar documentación en 5 tantos, 4 serán copias todas con carpeta antes especificada
</li>
</ul>
