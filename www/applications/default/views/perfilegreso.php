<p>PERFIL DE EGRESO:

Tomando en cuenta las necesidades de salud de la población, mercado de trabajo y objetivos institucionales, la Escuela de Enfermería de Apatzingán se propone preparar profesionistas con formación humanística, científico-tecnológica y con alto sentido crítico de compromiso social, que desarrolle una práctica consiente y solidaria con los grupos de riesgo y con la profesión misma. Por tal motivo., el perfil del egresado de la Carrera de Enfermería, describe las características deseadas que deberá poseer el alumno al finalizar su formación escolarizada; incluidos conocimientos, habilidades, destrezas y actividades que en conjunto definan y delimiten su ejercicio profesional a fin de contribuir, a la satisfacción de las necesidades sociales para las cuales fue formado, demostrando como profesional, capacidad para:

 

Proporcionar atención de calidad a la comunidad en los diferentes niveles de atención.
Contribuir en la aplicación del código de ética para los enfermeros y enfermeras de nuestro país.
Comprender la interrelación de los procesos sociales con los problemas y necesidades de salud de la población.
Integrarse a grupos de trabajo que le permitan desarrollar acciones en beneficio de la población.
Asumir actitud de liderazgo, organizando grupos para la atención de la salud comunitaria.
Participar en la educación para la salud comunitaria, con la implementación de acciones que estimulen las prácticas benéficas y contrarresten aquéllas que representan riesgos para la salud.
Contribuir a la conservación y mejoramiento del medio ambiente.
Integrarse a proyectos de investigación inter y multidisciplinarios, que contribuyan en la búsqueda de soluciones a problemas de la práctica profesional.
Enfrentar críticamente sus necesidades de salud, desarrollo personal y profesional.
Ejercer autonomía y creatividad dentro del ejercicio de su profesión conforme a la formación cognoscitiva de la misma.
Asimismo, se espera que establezca compromiso con:

La sociedad, para participar con un alto sentido de solidaridad en la búsqueda constante de mejores estándares de vida, a través del servicio social.
La profesión, para su desarrollo a través del análisis y replanteamiento del "ser" y "deber ser" de acuerdo al momento histórico. Mediante su participación activa en organismos gremiales.
La institución para apoyar experiencias que enriquezcan la currícula escolar. Su desarrollo personal y profesional a través de la constante búsqueda de experiencias.
Desarrollar los principios y valores que se enmarcan en el código de Ética para enfermeras y enfermeros del México.</p>
