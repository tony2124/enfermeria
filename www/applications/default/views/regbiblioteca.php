<p><b>CAPITULO I.-   PRESENTACION</b></p>
<p>&nbsp;</p>
<p>
	<b>ARTÍCULO 1.-</b> La biblioteca del Plantel es de gran importancia para los estudiantes y profesores de la Escuela de Enfermería de Apatzingán, AC. En ella se pueden consultar una gama inmensa de temas que conforman el plan de estudios de la carrera. Entre los aproximadamente 300 volúmenes con que cuenta la biblioteca hay un porcentaje muy alto de bibliografía básica  y de bibliografía complementaria que solicitan los profesores; también se cuenta con un acervo muy completo de obras de consulta, tales como enciclopedias, diccionarios, atlas, etc.
</p>
<p>
	<b>ARTÍCULO 2.-</b> Las instalaciones son pequeñas pero funcionales, para que los usuarios realicen sus trabajos. Proporcionamos servicios de internet, préstamo interno, y de consulta, de orientación a usuarios, de asesorías bibliográficas, entre otros que se mencionan. La demanda de servicios bibliotecarios es muy grande, por tal motivo tenemos que ser muy estrictos en el cumplimiento del reglamento.
</p>
<p>
	<b>ARTÍCULO 3.-</b> Es indispensable que todos los que participamos en la Biblioteca (  alumnos,  trabajadores  y autoridades)  sumamos el compromiso de cumplir con lo que nos corresponde; deseamos que la biblioteca sirva de apoyo fundamental para los estudiantes por lo cual debemos aprender, mostrar y vigilar lo que debemos y lo que no debemos hacer dentro de la misma.
</p>
